# 6. Crie um programa que converta uma temperatura de Celsius (entrada como
# float) para Fahrenheit usando a fórmula F = C * 9/5 + 32 e imprima o resultado.

Celsius = float(input("Digite uma temperatura em graus celsius:"))

Farenheit = Celsius * 9/5 + 32

print(f"Esta temperatura em Farenheit é {Farenheit}.")